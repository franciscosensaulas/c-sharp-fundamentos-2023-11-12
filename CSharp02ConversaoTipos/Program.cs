int numero = Convert.ToInt32("2"); // Convertendo uma string(texto) para int(número inteiro)
Console.WriteLine("Número: " + numero);


double preco = Convert.ToDouble("2056,90"); // Convertendo uma string(texto) para double(número real)
Console.WriteLine("Preço: " + preco);


bool vivo = Convert.ToBoolean("True"); // Convertendo uma string(texto) para bool(lógico)
Console.WriteLine("Vivo: " + vivo);

char letra = "Olá mundo"[0]; // Obtendo a primeira posição(0) do texto "Olá mundo", ou seja, a letra 'O'
Console.WriteLine("Letra: " + letra);

Console.Write("oLÁ MUNDO");